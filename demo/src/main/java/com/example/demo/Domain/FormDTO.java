package com.example.demo.Domain;

public class FormDTO {
    String vegan;
    String lowSodium;
    String lowKcal;
    String highProtein;
    String logSugar;
    String duration;

    public String getVegan() {
        return vegan;
    }

    public void setVegan(String vegan) {
        this.vegan = vegan;
    }

    public String getLowSodium() {
        return lowSodium;
    }

    public void setLowSodium(String lowSodium) {
        this.lowSodium = lowSodium;
    }

    public String getLowKcal() {
        return lowKcal;
    }

    public void setLowKcal(String lowKcal) {
        this.lowKcal = lowKcal;
    }

    public String getHighProtein() {
        return highProtein;
    }

    public void setHighProtein(String highProtein) {
        this.highProtein = highProtein;
    }

    public String getLogSugar() {
        return logSugar;
    }

    public void setLogSugar(String logSugar) {
        this.logSugar = logSugar;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }
}
